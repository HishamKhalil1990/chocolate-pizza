'use strict'

let cb1 = document.getElementById('cb1');
cb1.addEventListener('change',deletItem1)
function deletItem1(event){
    if (event.target.checked) {
        let p1 = document.getElementById('p1');
        p1.setAttribute('style','text-decoration: line-through')
      } else {
        let p1 = document.getElementById('p1');
        p1.removeAttribute('style');
      }
}

